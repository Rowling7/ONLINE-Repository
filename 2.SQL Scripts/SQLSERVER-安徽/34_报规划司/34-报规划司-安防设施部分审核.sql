--“是否已实施安防工程”为“1.已实施”时，“安全设施是否齐全”不能为“2.需要但缺失”
SELECT 
K0801,K0802,K0804,K0805,K0806,K0807,K0808,K0809,K0810,K0811,K0814,K0815
FROM K08 
WHERE (
K0804	=2 or
K0805	=2 or
K0806	=2 or
K0807	=2 or
K0808	=2 or
K0809	=2 or
K0810	=2 or
K0811	=2 or
K0814	=2 or
K0815	=2 
) and K0802='1'

